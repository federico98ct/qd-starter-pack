#
#   Write a program that given two numbers as input make the main operations.
#
#   Output:
#   Insert first number: 4
#   Insert second number: 2
#
#   SUM: 6
#   Difference: 2
#   Multiplication: 8
#   Division: 2
#

numero_1 = 4
numero_2 = 2

somma = numero_1 + numero_2
differenza = numero_1 - numero_2
moltiplicazione = numero_1 * numero_2
divisione = numero_1 / numero_2

print("somma:", somma, "\n", "differenza:", differenza, "\n", 
      "moltiplicazione:", moltiplicazione, "\n", "divisione:", divisione)


